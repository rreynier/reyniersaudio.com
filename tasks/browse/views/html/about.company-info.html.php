                <h1>A little bit about us</h1>
                <p>Reyniers Audio is based out of Denver, CO. We specialize in custom built recording computers. It is our goal to build the fastest, most
                    reliable Digital Audio Workstations (DAW) available. We strive to provide the best customer service and warranty for your purchase.</p>
                <p>All of us at Reyniers Audio are musicians and geeks. Collectively, we have years of experience in the studio, as both musicians and
                    engineers. The passion for both music and technology made it a natural choice to begin building PCs geared towards music. We started with
                    PCs for friends and DAWs for local bands who wanted to be able to record at home without having to pay thousands at a studio where time is
                    in short supply. The great thing about today's technology is that virtually anyone capable of saving some money can start his or her own
                    studio. Our expertise in building DAWs, tweaking them to their absolute finest, and the low cost of our builds will prove to make a very
                    wise purchase.</p>

                <h1>Our studio and workshop</h1>
                <p>From January 2009 to June 2011 our Research and Development center was at Crooked Tree Studios in Dade City, FL. Crooked Tree
                    is a 5,000 square foot facility which functions as a fully operational studio. This is where all the real world testing happens.
                </p>
                <p>As of June 2011, Reyniers Audio moved its operations to Denver, Colorado.  This has put us closer to our clients allowing us to provide
                    faster service and improved customer support.
                <p><a href="http://www.myspace.com/middlerhythm" target="_blank">The Middle Rhythm Session</a> band rehearses at Crooked Tree every
                    Tuesday and Sunday. During rehearsals we are always finding ways to make things more efficient and better sounding. Using a Reyniers Audio
                    workstation armed with the RME HDSP9652 interface and Reaper software we are able to record all 32 inputs required by the band. </p>
                <p>We do everything in the box, meaning all the tracking, mixing and mastering is done within Reaper and without any outboard gear. Of course
                    each band member gets his own personal mix and each track has its own EQ and compressor plugins. We sprinkle in a vast array of reverbs,
                    triggers, side chains and delay busses to really create an immersive sound that both the audience and the band can get lost into. We do all
                    of this in real time. No glitches, no pops, just like it should be.	</p>
