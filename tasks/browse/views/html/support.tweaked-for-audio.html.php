                <h1>Tweaked for Audio</h1>
                <p>Reyniers Audio Workstations come fully tweaked for music production. As you may already know, Windows operating systems come quite bloated when first installed. The advantage of getting your recording PC from Reyniers Audio is that we make sure all services not needed for music are disabled and ones needed to optimize performance for music production and intensive hard disk recording are enabled. When you receive your DAW computer there is no need to make any adjustments. Simply unpack your cmputer, install your software and start making music.</p>
                <h1>How do we optimize Windows?</h1>
                <h2>Unnecessary services disabled:</h2>
                <p>Windows 7 stock comes with many services (background processes) that are not necessary for most computing purposes.</p>
                <h2>Windows system restore disabled:</h2>
                <p>This allows you to do system restores within Windows. However, even when it isn't being used to backup or restore system setting,
				system restore will still eat up cpu cycles, memory and hard drive space.  This could easily interfere when multitracking or 
				when using VST(i).  Turning off this feature will improve performance drastically. The included Acronis TrueImage software is a
				much better alternative for creating system restore images as it can be done outside of Windows. </p>
                <h2>Windows automatic updates:</h2>
                <p>Even though it would seem like a good idea to stay up to date on your Windows install, many of the updates are unecessary
				for audio production.  In fact some updates may even be counterproductive with regards to low audio latency.  Furthermore, the Windows Automatic Upate service polls Microsoft.com regularly to find out if new patches 
				are available, and if they are it will download and install them. If this happens while recording, it can cause hiccups.</p>
                <h2>Print spooler:</h2>
                <p>This saves on RAM and CPU cycle as it will not poll for printer information regularly. This will disable
				printer functionality although it is very easy to turn on in case a printer is needed.</p>
                <h1>System Recovery Package:</h1>
                <p>Every system we sell comes with a full version of Acronis TrueImage which facilitates all backup and restore procedures.</p>
                <p>Once we've completed the burn-in procedure, tweaked the system and the computer is ready to be shipped, we create a System Recovery
				Package. We include instructions to restore to this "Factory Image" as well as ways for you to create your own Restore Images after 
				all your software is installed.</p>
                <h1>Drivers and Updates:</h1>
                <p>We install and tweak the Operating System for you and because we realize it's best to isolate DAW computers from the Internet we go
				through all the Windows Update Patches and install the most recent, relevant updates.</p>
                <p>We flash the BIOS on your motherboard to the latest most stable version - this can often make the difference between a smooth operating,
				reliable DAW and one that isn't quite so.</p>
                <p>We install the latest drivers for all the system components. By request, we can also install drivers for audio interfaces you plan on
				using with the system. If you have us install your drivers, all you need to do when you receive the computer is plug in your interface 
				and start recording.</p>			
                <h1>Browsing the internet with Reyniers Audio workstations:</h1>
                <p>Digital Audio Workstation computers should generally not be connected to the internet as you're bound to accidentally install Spyware/Adware
				that will bring your system to a crawl.  To ensure the system stays in its optimal shape, it is wise not to browse the Internet other than for 
				software updates and registration.</p>			
