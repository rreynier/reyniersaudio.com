                        <p>We use only top quality hardware for both performance and reliability. We use quiet and efficient cooling systems, a luxury
                            rarely afforded with other name brand systems. Our top tier systems are designed specifically with future upgrades and reliability
                            in mind so your system will not go obsolete the second it arrives at your door. Our systems are optimized for speed. This is done
                            by pairing parts that have shown to be fast and reliable and by minimizing Windows' footprint on your CPU and RAM.</p>
                    </div>

                    <h2>Why does a Digital Audio Workstation cost more than a normal PC?</h2>
                    <div>
                        <p>Recording music demands calculations from a computer that other applications don't. Large computer manufacturers do not want to tailor a
                            system to your specific purpose, because that would mean the cost of the PC would go up. Since they care more about quantity of sales,
                            their systems are built to please the average computer user. Our DAWs use processors that are specifically fast at doing the calculations
                            common to effects, VST(i)s, etc. Our components are matched for best performance and tweaked to the max. From us, you will get a very lean
                            Windows install tweaked for performance. When you receive your computer, you will not need to spend hours getting your machine to run properly.
                            When you get your Reyniers Audio computer it will come quiet, fast, tweaked and ready to work.</p>
                    </div>

                    <h2>How do you test your systems?</h2>
                    <div>
                        <p>Each computer that leaves our workbench goes through very thorough tests. We examine every single byte of memory on your RAM chips.
                            We 'burn in' the CPU, which means we push the processor to its absolute capacity for hours on end (on a faulty or insufficiently cooled
                            CPU this often leads to crashes and other various issues). We measure the temperature and fan speeds. If the temperature of the CPU or
                            case goes over a certain threshold we replace the necessary parts to keep the computer cool and quiet. We also test the Hard Drives
                            sector by sector. We stress-test your machine for 24 hours to ensure there are absolutely no questions about your computer's stability.</p>
                    </div>

                    <h2>Do I get a licensed, legitimate copy of Windows XP/Vista?</h2>
                    <div>
                        <p>Of course. It would be illegal for us to sell Windows with our computers without giving you a properly licensed version. We install and
                            authenticate Windows for you, which allows us to apply the latest Windows Updates so that your system has the latest security and performance
                            patches installed.</p>
                    </div>

                    <h2>Which is better, desktops or laptops?</h2>
                    <div>
                        <p>That depends. Desktops and laptops are built with different applications in mind. To complicate matters, laptops come in different
                            configurations as well. As far as simply tracking audio, a properly configured laptop can do anything a comparable desktop can do. If
                            you are looking for real-time, super-sampled VST instruments, a desktop will do a much better job as its hard drives, memory, and in
                            many cases the CPU, are much faster. Desktops are less expensive and easier to customize; however, they are more difficult traveling buddies.</p>
                    </div>

                    <h2>Will my Reyniers Audio Computer work with my hardware and software?</h2>
                    <div>
                        <p>Yes, your Reyniers Audio Computer should work with your existing audio interface and DAW software. When we design our workstations our
                            first priority is to build systems that are compatible with the industry's hardware and software. We achieve this by testing each
                            individual component with all the major software/hardware options available on the market. The idea is that when you receive the
                            computer all you need to do is plug it in, install your software and record. We can even install your hardware/software for you at
                            no additional cost. Simply give us a call and we will coordinate the shipment of your hardware/software to us to limit your studio
                            downtime.  <br/><a href="/support/compatibility">Compatibility</a>
                        </p>
                    </div>

                    <h2>Is it safe to connect my Reyniers Audio Computer to the internet?</h2>
                    <div>
                        <p>It is generally recommend to keep your Digital Audio Workstation offline. There are too many things on the internet that could cripple
                            your system, making it slower and less reliable. It is ok, however, to connect your DAW to the internet for hardware and software updates
                            or online registrations of plugins, hosts etc. If you plan on using your Reyniers Audio Workstation for general internet purposes such as
                            email or web browsing, we highly recommend using an alternate browser such as Mozilla Firefox and installing anti-virus and anti-spyware
                            software. </p>
                    </div>

                    <h2>Can I install anti-virus and anti-spyware software on my Reyniers Audio Computer?</h2>
                    <div>
                        <p>Yes, but keep in mind that Digital Audio Recording software and recording with the PC in general is very resource intensive.
                            You generally want to avoid having other software (such as anti-virus/spyware software) use up unnecessary CPU cycles and memory.
                            Because security software constantly checks the state of your system to ensure it stays clean they tend to bog down your time-critical
                            operations such as multi-track recording. So is it possible? Yes. Recommended? No. </p>
                    </div>
