                <h1>Our systems lead the industry in compatibility</h1>
                <p>As we run our own professional recording studio, Crooked Tree, we get the chance to test the software and hardware used in the music industry.
				As we find incompatibilities, we tweak our configurations to ensure everything works well together. 
				We pride ourselves on being compatible with virtually all DAW software, including Pro Tools, Nuendo, Sonar and Samplitude, to name just a few.</p>
                <h1>Windows 7 - 64-bit Edition</h1>
                <p>Although our recommended choice for Operating System is still the 'lean &amp; mean' Windows XP Home,
			we've tested Windows 7 64-bit extensively on all our configurations and are happy to report all works well. This next generation operating system finally breaks the 4GB 
			memory barrier.	The list of 64-bit capable audio software and hardware is growing steadily. </p>

